<h3><span>W</span>hat is Artgoobi</h3>
<p>
    Artgoobi, an art-based social web portal conceived as an interactive site,
    provides a free virtual space to artists/art collectors to share their artworks and profiles individually and independently. 
</p>
<p>
    A concern of ‘180 Degrees’, it is focused on people involved in art practice and art collection,
    who can join this site to exhibit his/her own creations and thoughts as well as collections. 
</p>
<p>
    Anyone with an interest in art and a developing oeuvre, or an already rich collection, is eligible to sign up and create their own profile. 
    Artgoobi would serve as hub for artists and collectors, therefore profile owners can stay
    connected with other artists and collectors through their respective accounts and through exchange of information on this site. 
</p>
<p>
    This virtual art village provides an opportunity for the artists not only to sell their works but also to create event/exhibition and update their  profile by way of adding news, views and event. Similarly, collectors can put their works on sale and use the facility of online auctions. 

    Here at Artgoobi, registered visitors can contact and buy artworks directly from the artists/collectors who are account holders and/or can get authentic buying-selling assistance from the Artgoobi team. Artgoobi team will also arrange virtual exhibitions, art events, auctions, event calendar and archive as well as develop an online art journal to lend impetus to the art scene.
</p>
