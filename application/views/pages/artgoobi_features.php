<h3><span>A</span>rtgoobi Features</h3>
<ul>
    <li>
        Artist/collector or gallery profile should contain artworks, writeups, news and reviews.

    </li>
    <li>
        Exhibition(s) should contain slideshow of a particular exhibition content during the period its being showcased at any gallery. Sale of the works will not commence before the end of the exhibition in an actual gallery space.  

    </li>
    <li>
        An event must contain a list of ongoing and upcoming events regarding art and culture.

    </li>
    <li>
        Audio-visual documentation should contain documentary, review and interview, etc. 

    </li>
    <li>
        Art Journal must have relevant articles, features, reviews etc. 
    </li>
    <li>
        Display of an art collection can contain slideshow of a particular exhibition content set by the owner and sale of works too shall be determined by the owner.
    </li>
    <li>
        Artgoobi will resort to a board of consultants and settle any appeal for falsification or duplication through them.
    </li>
</ul>
