                <h1>Manan Morshed</h1>
                <h2>Chairman</h2>
                <p class="member_details" id="detals_02">
                    Born 1976, Manan Morshed received his Bachelor of Fine Arts in 1999, from the Institute of Fine Arts (now, Faculty of Fine Arts), University of Dhaka. He started his career as graphic artist simultaneously in different fields. Other than a artist, he also played significant role(s) as a photographer, organizer, activist and entrepreneur.
                    Manan Morshed worked as artist at ‘The Daily Star’, the leading English daily newspaper, for 19 years (2001-2018) and experienced multidisciplinary participation, coordination, public relation and management of 'Celebrating Life' by The Daily Star, through the successive seasons Simultaneously, he provides art consultancy to’ Steps Towards Development’, a national NGO focused on human rights, since 2000. 
                    He founded ‘Arka Communications Limited’ in 2004, an advertisement firm, and playing vital role as Chairman and Chief Creative. He had managed the three-day-long large-scale international event of 'South Asia Social Forum', simultaneously at 19 different venue in Dhaka University Campus, and many other social events.
                    Between 2010-2017 Manan Morshed presented fine arts in a artistic manner while worked as Art Director at ‘Depart’, the art magazine. 
                    Had launched and organized (four years) yearly Literature and Cultural Competition for new comers at University of Dhaka, in absence of DUCSU.
