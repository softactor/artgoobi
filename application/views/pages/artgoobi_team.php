                <h1>Mustafa Zaman</h1>
                <h2>Chief Curator</h2>
                <p class="member_details" id="detals_01">
                    Born 1968, Mustafa Zaman received his Bachelor of Fine Arts in 1989, from the Institute of Fine Arts (now, Faculty of Fine Arts), University of Dhaka.
                    Trained as a printmaker, he soon veered into multidisciplinary practice turning his attention to contemporary human condition 
                    often observed in relation to the instruments of power.
                    As a writer Mustafa started contributing art reviews to The Observer Magazine since the mid 1990s. He joined The Daily Star in 2002 and worked
                    in the scope of a feature writer for Star Weekend Magazine for about three years. He has contributed numerous art reviews and articles
                    to a number of vernacular Dailies including Bhorer Kagoj and Prothom Alo on both emerging and established Bangladeshi artists. Mustafa has written numerous prefaces
                    to exhibition catalogues in the last fifteen years.
                    Mustafa was editor of Depart, an art magazine that ran from 2010 to 2017. Mustafa’s major curatorial efforts include CrossOver (2011-2012),
                    two back-to-back workshops and exhibitions planned and sponsored by Art & Bangladesh, the organization responsible for Depart, with artists from
                    India and Bangladesh as participants; two solo exhibitions in 2013  including DeReal by Bahram, a rickshaw painter who crossed over to mainstream art;
                    and Gravity Free World by expatriate artist A Rahman; a retrospective exhibition in 2014 entitled In(site) by Kazi Salahuddin Ahmed.
                    Mustafa was the curator of the Garmeenphone art profiling in their newly built headquarters.
                </p>
